	schedule_start_times_in_usecs int8 NULL,
	schedule_end_time_in_usecs int8 NULL,
	schedule_local_max_snapshots int4 NULL,
	schedule_remote_max_snapshots jsonb NULL,
	exclusive_usage_in_bytes int8 NULL,
	snapshot_id varchar(255) NULL,
	snapshot_create_time_usecs int8 NULL,
	snapshot_expiry_time_usecs int8 NULL,
	schedule_user_start_time_in_usecs int8 NULL,
	schedule_id varchar(255) NULL,
	schedule_suspended bool NULL,
	schedule_local_retention_period int8 NULL,
	schedule_remote_retention_period jsonb NULL,
	schedule_local_retention_type varchar(255) NULL,
	schedule_remote_retention_type varchar(255) NULL,
