CREATE TABLE public.nutanix_snapshot_schedule_metrics (
	collection_time timestamp DEFAULT CURRENT_TIMESTAMP NULL,
	nutanix_ip varchar(255) NULL,
	protection_domain_name varchar(255) NULL,
